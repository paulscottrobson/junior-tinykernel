 	*= $F000

NMIHandler:
IRQHandler:
		rti

SystemReset:
		jmp 	SystemReset

				
; ********************************************************************************
;
;									6502 Vectors
;
; ********************************************************************************

	* =	$FFFA

	.word 	NMIHandler                       	; nmi ($FFFA)
	.word 	SystemReset                         ; reset ($FFFC)
	.word 	IRQHandler                          ; irq ($FFFE)

	.end 
